    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String s1 = jTextField1.getText();
            String s2 = jPasswordField1.getText();
            String s3 = jPasswordField2.getText();
            String s4 = jLabel7.getText();
            if(s2.equals(s3)){
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into studentLogin values (?,?,?)");
                ps.setString(1, s1);
                ps.setString(2, s2);
                ps.setString(3, s4);
                ps.executeUpdate();
                JFrame jf = new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf, s1+"Sucessfully Registered");
                setVisible(false);
                new loginStudent().setVisible(true);
            }
            else{
                JFrame jf = new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf, "Confirm Pasword doesn't match");
            }
        }
        catch(Exception e){
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

